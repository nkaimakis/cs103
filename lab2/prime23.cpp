#include <iostream>

using namespace std;

int main()
{
//define variables
   int num;
   int twos = 0;
   int threes = 0;
   
   cout << "Enter a positive integer:" << endl;
   cin >> num;



   while(num % 3 == 0)
   {
      num = num / 3;
      threes++;
   }

   while(num % 2 == 0)
   {
      num = num / 2;
      twos++;
   }
   
   if(num == 1)
   {
    //print counts
    cout << "Yes" << endl << "Twos=" << twos << " Threes=" << threes << endl;  
    }          
   
   else   
   {     
      cout << "No" << endl;
   }
   

            

return 0;
}
